<p class="meta"><span class="date">Расчет аннуитетного платежа</span></p>
<div class="entry2">
<p>
<b>Аннуитетными</b>, т.е. равновеликими платежами называют платежи, которые производятся на протяжении всего срока кредита равными друг другу. При таком виде платежа заемщик регулярно совершает платеж одного и того же размера. Эта сумма может меняться только по соглашению сторон или в некоторых случаях частичного досрочного погашения. Структура аннуитетного платежа также состоит из двух частей: процентов за пользование кредитом и суммы идущей на погашение кредита. С течением времени соотношение этих величин меняется и проценты постепенно начинают составлять меньшую величину, соответственно сумма на погашение основного долга внутри аннуитетного платежа увеличивается. Поскольку, при аннуитетных платежах в начале сумма, идущая на погашение основного долга, убывает медленно, а проценты всегда начисляются на остаток от этой суммы, то и общий размер уплаченных процентов по такому кредиту больше. Это особенно заметно при досрочных погашениях. В первые периоды кредитования основные выплаты приходятся именно на погашение процентов по кредиту. 
Величина аннуитетного платежа определяется по формуле:
</p>
<p><em>Формула 4.</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img src="images/raschet009.png"  alt="Аннуитетные платежи" align="center" />&nbsp;&nbsp;&nbsp;, где  </p>
<p>
<b>АП</b> — <em>аннуитетный платеж;  </em> <b>ПС</b> — <em>процентная ставка за период начисления; </em> <b> СК</b> — <em>первоначальная сумма кредита; </em>
<b>КП</b> — <em>количество периодов.</em> </p>
<p>
<b>!</b> <em>Т.е. если платежи ежемесячные, то КП – срок в месяцах, а ПС месячная процентная ставка (1/12 годовой)</em> 
</p>
<p>
<b>Формулу 4 </b> <em>можно назвать «классической», т.к. она применяется в расчетах, где все платежи аннуитетные, она применяется в большинстве банков, кредитных калькуляторах, в электронных таблицах. Так же она используется в расчетах на сайте <a href="http://Credit-talk.ru">Credit-talk.ru</a></em> 
</p>
<p>
Расчет аннуитетных платежей по этой формуле, можно производить с помощью MS Excel и встроенной функции рабочего листа PMT (в русских версиях ППЛАТ или ПЛТ)
</p>
