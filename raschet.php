<p class="meta"><span class="date">Расчет дифференцированного платежа</span></p>
<div class="entry2">
<p><b>Дифференцированные платежи</b> в начале срока кредитования больше, а затем постепенно уменьшаются, т.е. регулярные платежи по кредиту не равны между собой. Структура дифференцированного платежа состоит из двух частей: фиксированной на весь период суммы, идущей на погашение суммы задолженности, и убывающей части — процентов по кредиту, которая рассчитывается от суммы остатка заложенности по кредиту. Из-за постоянного уменьшения суммы долга уменьшается и размер процентных выплат, а с ними и ежемесячный платеж. </p>
<p>Для того чтобы вычислить сумму возврата основного долга, необходимо первоначальную сумму кредита разделить на срок кредита (количество периодов):
</p>
<p><em>Формула 1.</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img src="images/raschet002.png"  alt="Дифференцированные платежи" align="center" />&nbsp;&nbsp;&nbsp;, где  </p>
<p>
<b>ОД</b> — <em>возврат основного долга;</em> <b>СК</b> — <em>первоначальная сумма кредита;</em> <b>КП</b> — <em>количество периодов.</em>
</p>
<p>
На этом сходство в подходах банков заканчивается, и начинаются различия. Состоят они в подходах к вычислению суммы причитающихся процентов. Основных подходов два, разница — в используемой временной базе. Часть банков исходят из того, что «в году 12 месяцев», и тогда размер ежемесячных процентных выплат определяется по формуле:
</p>
<p><em>Формула 2.</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img src="images/raschet004.png"  alt="Дифференцированные платежи" align="center" />&nbsp;&nbsp;&nbsp;, где  </p>
<p>
<b>НП</b> — <em>начисленные проценты; </em> <b>ОК</b> — <em>остаток кредита в данном месяце;</em> <b> ПС</b> — <em>годовая процентная ставка.</em>
</p>
<p>Часть банков исходит из того, что <b>«в году 365 дней»</b> и такой подход называется расчетом точных процентов с точным числом дней ссуды. Размер ежемесячных процентных выплат в данном случае определяется по формуле:
</p>
<p><em>Формула 3.</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img src="images/raschet006.png"  alt="Дифференцированные платежи" align="center" />&nbsp;&nbsp;&nbsp;, где  </p>
<p>
<b>НП</b> — <em>начисленные проценты; </em> <b>ОК</b> — <em>остаток кредита в данном месяце;</em> <b> ПС</b> — <em>годовая процентная ставка;</em>
<b>ЧДМ</b> — <em>число дней в месяце (понятно, что это число меняется от 28 до 31).</em> 
</p>
<p>
