<p>
<b>Другие формулы для расчета аннуитетного платежа</b></p>
<p>Некоторые кредитные организации применяют
<b>формулу, где первый платеж - не аннуитетный:</b></p>
<p><em>Формула 5.</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img src="images/raschet012.png"  alt="Аннуитетные платежи" align="center" />&nbsp;&nbsp;&nbsp;, где  </p>
<p>
<b>АП</b> — <em>аннуитетный платеж;  </em> <b>ПС</b> — <em>процентная ставка за период начисления; </em> <b> СК</b> — <em>первоначальная сумма кредита; </em>
<b>КП</b> — <em>количество периодов.</em> </p>
<p>Первый платеж предварительный - не аннуитетный. Он всегда, якобы, меньше АП, т.к. включает в себя только проценты за первый период, который может быть полным или неполным. Но при полном периоде - 31 день, при высоких ПС и долгосрочном кредитовании предварительный платеж может быть больше АП! Оставшиеся (КП-1) платежей – аннуитетные. Эта формула используется в АИЖК.</p>
<p>Также на практике встречается применение формулы, где <p>первый и последний платежи – не аннуитетные:</b> </p>
<p><em>Формула 6.</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img src="images/raschet014.png"  alt="Аннуитетные платежи" align="center" />&nbsp;&nbsp;&nbsp;, где  </p>
<p>
<b>АП</b> — <em>аннуитетный платеж;  </em> <b>ПС</b> — <em>процентная ставка за период начисления; </em> <b> СК</b> — <em>первоначальная сумма кредита; </em>
<b>КП</b> — <em>количество периодов.</em> </p>
<p>Первый и последний платежи не аннуитетные, первый - только проценты за первый период, а последний - остатки, "хвосты" и т.д. </p>
<p>Оставшиеся (КП - 2) платежей - аннуитетные. Видимо, банки подгоняют АП под целое число рублей или долларов. Поэтому образуется "хвост", который переходит на последний не Аннуитетный Платеж. Далее после каждого досрочного погашения банки подгоняют уже новый уменьшенный АП под целое число денежных единиц. Т.е. "хвост" может уменьшаться или увеличиваться.Эта формула используется в ВТБ24.</p>
<p><b>Наименьший Аннуитетный Платеж</b> получается при расчетах по <b>формуле 4</b>, наибольший - по формуле 6. 
Причем чем меньше АП остается до окончательного расчета, тем существеннее становится эта разница. 
Что особенно важно при досрочном погашении. 
Поэтому <b>необходимо интересоваться не только процентной ставкой, но и формулой</b> по которой рассчитываются АП.
</div>
<p class="meta"><span class="date">Что выгоднее аннуитетная или дифференцированная схема платежей?</span></p>
<div class="entry2">
<p>Вопросам выбора схемы платежа по ипотечному кредиту часто задаются потенциальные заемщики. Если сравнивать аннуитетную и дифференцированную схемы, то самыми очевидными различиями будут являться следующие:</p>
<ul>
						<li><p><b>Неизменность размера</b> регулярного платежа при аннуитетной схеме и <b>постоянное убывание</b> такого платежа при дифференцированной.</p></li>
						<li><p><b>Больший размер платежа</b>, по сравнению с аннутетной схемой, в начале срока кредита при дифференцированной схеме.</p></li>
						<li><p><b>Аннуитетная схема выплат более доступна</b> для заемщиков, т.к. <b>выплаты равномерно распределяются</b>на весь срок кредита. При выборе дифференцированных платежей подтвержденный доход заемщика или созаемщиков должен быть <b>примерно на четверть больше</b>, чем при аннуитетных платежах.</p></li>
						<li><p>При аннуитетных платежах в начале сумма основной задолженности убывает медленно, а и <b>общий размер начисленных процентов больше</b>. Если заемщик решит полностью погасить кредит досрочно, выплаченные вперед проценты будут потеряны. При аннуитетной схеме значительная часть процентов уплачивается с начала, обеспечивая выплаты на весь срок кредита. Поэтому при дифференцированных платежах досрочное погашение будет происходит без таких финансовых потерь даже в начале срока ипотечного кредита.</p></li>
						<li><p><b>Кредит с дифференцированным платежом труднее получить</b>, т.к. при получении кредита оценивается платежеспособность заемщика. Дифференцированная схема в начале срока кредита предлагает значительно большие платежи, нежели аннуитетная. Это означает то, что заемщику необходимо иметь больший доход. В среднем считается, что доход заемщика при дифференцированной схеме должен быть больше на 20% выше, чем при аннуитетной схеме.</p></li>
					</ul></p>
<p>Подводя итог можно сказать, что вид платежа является одним из основных параметров кредита, однако рассматривать его необходимо в совокупности с другими параметрами.</p>
